def check_vowels():
    name1 = input()

    print(f"Contiene a: {("a" in name1.lower())}")
    print("Contiene e: " + str(("e" in name1.lower())))
    print(f"Contiene i:  {("i" in name1.lower())}")
    print(f"Contiene o:  [{("o" in name1.lower())}")
    print(f"Contiene u:  {("u" in name1.lower())}")
